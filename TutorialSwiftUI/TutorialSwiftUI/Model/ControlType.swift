import Foundation

enum ControlType: String, CaseIterable {
    case button = "Button"
    case menu = "Menu"
    case contextMenu = "Context Menu"
    case toggle = "Toggle"
    case slider = "Slider"
    case stepper = "Stepper"
    case picker = "Picker"
}
