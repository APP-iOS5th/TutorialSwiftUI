import Foundation

enum LayoutType: String, CaseIterable {
    
    case space = "Spacer"
    case padding = "Padding"
    case frame = "Frame"
    case positionPoint = "Position Point"
    case geometryReader = "Geometry Reader"
    case equalSizeViews = "Equal size views"
    
}
