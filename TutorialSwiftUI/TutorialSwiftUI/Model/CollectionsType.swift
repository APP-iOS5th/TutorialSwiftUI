import Foundation


enum CollectionsType: String, CaseIterable {
    case vStack = "VStack"
    case hStack = "HStack"
    case zStack = "ZStack"
    case grid = "Grid"
    case lazyVGrid = "LazyVGrid"
    case lazyHGrid = "LazyHGrid"
    case scrollView = "Scroll View"
    case tabView = "TabView"
}
